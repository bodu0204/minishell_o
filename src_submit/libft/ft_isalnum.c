int	ft_isalnum(int	c)
{
	int	j;

	j = 0;
	j += (c >= 'A' && c <= 'Z');
	j += (c >= 'a' && c <= 'z');
	j += (c >= '0' && c <= '9');
	if (j)
		return (1);
	return (0);
}
