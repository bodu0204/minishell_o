#include "libft.h"

char	*ft_strpbrk(const char *s, const char *charset)
{
	size_t	i;
	size_t	j;

	i = 0;
	while (s[i])
	{
		j = 0;
		while (charset[j])
		{
			if (s[i] == charset[j])
				return ((char *)s + i);
			j++;
		}
		i++;
	}
	return (NULL);
}
