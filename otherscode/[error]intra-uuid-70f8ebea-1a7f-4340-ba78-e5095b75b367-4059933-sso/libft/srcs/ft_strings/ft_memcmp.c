#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t				i;
	const unsigned char	*us1;
	const unsigned char	*us2;

	us1 = (const unsigned char *)s1;
	us2 = (const unsigned char *)s2;
	i = 0;
	if (n == 0)
		return (0);
	while (us1[i] == us2[i] && i < (n - 1))
		i++;
	return (us1[i] - us2[i]);
}
