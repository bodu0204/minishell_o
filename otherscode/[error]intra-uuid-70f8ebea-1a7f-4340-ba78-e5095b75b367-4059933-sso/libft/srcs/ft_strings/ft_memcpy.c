#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	unsigned char		*udest;
	const unsigned char	*usrc;

	if (dest == src)
		return (NULL);
	udest = (unsigned char *)dest;
	usrc = (unsigned char *)src;
	while (n > 0)
	{
		*udest++ = *usrc++;
		n--;
	}
	return (dest);
}
