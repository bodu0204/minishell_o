#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char		uc;
	const unsigned char	*us;

	uc = (unsigned char)c;
	us = (const unsigned char *)s;
	while (n > 0)
	{
		if (*us == uc)
			return ((void *)us);
		us++;
		n--;
	}
	return (NULL);
}
