#include "libft.h"

char	*ft_strndup(const char *s1, size_t n)
{
	char	*s2;
	size_t	i;
	size_t	s1_len;

	s1_len = ft_strnlen(s1, n);
	if (s1_len < n)
		n = s1_len;
	s2 = (char *)malloc(n + 1);
	if (s2 == NULL)
		return (NULL);
	i = 0;
	while (s1[i] && i < n)
	{
		s2[i] = s1[i];
		i++;
	}
	s2[i] = '\0';
	return (s2);
}
