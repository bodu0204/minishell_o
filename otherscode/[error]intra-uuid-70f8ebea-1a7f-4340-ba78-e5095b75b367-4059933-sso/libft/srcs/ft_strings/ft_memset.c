#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char	*ub;

	ub = (unsigned char *)b;
	while (len > 0)
	{
		*ub++ = (unsigned char)c;
		len--;
	}
	return (b);
}
